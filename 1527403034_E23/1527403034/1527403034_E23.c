#include<stdio.h>
int main()
{
	int a=0,b=0,c=0;
	//鸡翁
	for(a=0;a<=20;a++)
	{
		//鸡母
		for(b=0;b<=33;b++)
		{
			//鸡雏
			for(c=0;c<=300;c++)
			{
				//判断百钱百鸡
				if((a+b+c==100)&&(5*a+3*b+c/3.0==100))
				{
					//输出
					printf("鸡翁%d只，鸡母%d只，鸡雏%d只\n",a,b,c);
				}
			}
		}
	}
	return 0;
}